#include <iostream>
#include <math.h>

using namespace std;

int main() {

	int num = 15;

	cout << "Valor de NUM em decimal: " << num << endl;
	cout << "Valor de NUM em hexadecimal: " << hex << num << endl; //o hex converte a variável inteira decimal em hexadecimal
	cout << "Valor de NUM em octadecimal: " << oct << num << endl; //o oct converte a variável inteira decimal em octadecimal
    cout << "Valor de NUM em decimal: " << dec << num << endl; //caso o numero seja hexa ou octa ou qualquer outro

	return 0;
}

