#include <iostream>

int main() {
    std::cout << "Hello, World!" << std::endl;
    system("pause");
    return 0;
}
// de acordo com Padrão ANSI
/*

#include - para inclusão de bibliotecas

<iostream> - biblioteca padrão inicial

using namespace std; - é usado para que não se repita o std em todos os comandos padrões da linguage (assim como deixei com cout e endl)

pode ser feito a funsão 'void' que é = nulo (no lugar de int main())

system("pause"); - faz o programa parar no cmd quando executado fora do code::blocks ou do terminal do VSCode

além do 'endl' também pode se usar o '\n' para quebra de linha (mas o \n é usado dentro das aspas duplas "")
*/
