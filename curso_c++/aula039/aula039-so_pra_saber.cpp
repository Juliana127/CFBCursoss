#include <iostream>

using namespace std;

int main() {

	cout << "Inteiro..........: " << sizeof(int) << endl;
	cout << "Caracter.........: " << sizeof(char) << endl;
	cout << "String...........: " << sizeof(string) << endl;
	cout << "Booleana.........: " << sizeof(bool) << endl;
	cout << "Flutuante pequeno: " << sizeof(float) << endl;
	cout << "Flutuante grande.: " << sizeof(double) << endl;

	return 0;
}
